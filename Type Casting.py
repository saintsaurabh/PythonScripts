# ComplexNumber into int,float,boolean,str(a+-bj)

# int
print("\nIntger")
print(complex(4))
print(complex(56,-18))

# float
print("\nFloat")
print(complex(23.21))
print(complex(-43.34,29.51))

#boolean
print("\nBoolean")
print(complex(True, False))
print(complex(False, True))
print(complex(True, True))
print(complex(False, False))

# str
print("\nString")
print(complex("61.12-73j"))
